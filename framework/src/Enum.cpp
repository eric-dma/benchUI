		GLboolean needs_blend_color(const GLenum func)
		{
#if (GLES_VERSION != 11)

			switch (func) {
			case GL_CONSTANT_COLOR:
			case GL_ONE_MINUS_CONSTANT_COLOR:
			case GL_CONSTANT_ALPHA:
			case GL_ONE_MINUS_CONSTANT_ALPHA:
				return GL_TRUE;
			default:
				return GL_FALSE;
			}
#else
			return GL_TRUE;
#endif
		}

